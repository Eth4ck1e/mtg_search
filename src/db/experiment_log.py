"""Write evaluation runs to the experiment_runs table.

One call to :func:`log_experiment` inserts one row. Each row captures
the full configuration that produced an evaluation plus the metrics it
scored; the final paper's results section is generated by aggregating
this table.

Keep this module thin. Validation of config/metrics shape belongs to the
evaluator that calls it — the JSONB columns are JSONB precisely so the
schema does not have to change as the pipeline gains new knobs.
"""

from __future__ import annotations

from dataclasses import dataclass
from datetime import datetime
from typing import Any

import psycopg
from psycopg.types.json import Jsonb

from src.config import settings

_INSERT_SQL = """
    INSERT INTO experiment_runs (
        eval_set_version, config, metrics, per_query, notes
    )
    VALUES (%s, %s, %s, %s, %s)
    RETURNING id, created_at
"""


@dataclass(frozen=True)
class ExperimentRecord:
    """Handle to a freshly-inserted experiment_runs row."""

    id: int
    created_at: datetime


def log_experiment(
    *,
    eval_set_version: str,
    config: dict[str, Any],
    metrics: dict[str, Any],
    per_query: list[dict[str, Any]] | None = None,
    notes: str | None = None,
) -> ExperimentRecord:
    """Insert one experiment_runs row.

    Args:
        eval_set_version: Identifier of the eval set used (e.g. ``"eval-v3"``).
            Required so a metrics number can always be traced back to the
            queries that produced it.
        config: The full configuration dict that produced ``metrics`` —
            model name, prompt version, preprocessing version, K, filter
            flags, anything that, if changed, would change the metrics.
        metrics: Aggregate metrics. Conventionally ``recall@1``, ``recall@5``,
            ``recall@10``, ``mrr``, ``latency_p50``, ``latency_p95``.
        per_query: Optional list of per-query results. Convention:
            ``[{"query": str, "expected_ids": list[str],
                "retrieved_ids": list[str], "hit_rank": int | None,
                "latency_ms": float}, ...]``.
        notes: Optional free-text human annotation.

    Returns:
        ExperimentRecord with the assigned ``id`` and ``created_at``.
    """
    params = (
        eval_set_version,
        Jsonb(config),
        Jsonb(metrics),
        Jsonb(per_query) if per_query is not None else None,
        notes,
    )
    with psycopg.connect(settings.database_url) as conn, conn.cursor() as cur:
        cur.execute(_INSERT_SQL, params)
        row = cur.fetchone()
    assert row is not None, "RETURNING clause should always yield a row on success"
    return ExperimentRecord(id=row[0], created_at=row[1])
